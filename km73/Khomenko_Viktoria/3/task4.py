'''
Умова: N студентів отримали K яблук і розподілити їх між собою порівну.
Неподілені яблука залишились у кошику. Скільки яблук отримає кожен студент? Скільки яблук залишиться в кошику?
'''
N = int(input('N=')) #Кількість студентів
K = int(input('K=')) #Кількість яблук
print("Кількість яблук які отримає кожен студент:", K//N, '\n', "Кількість яблук які залишаться у кошику:", K%N)




